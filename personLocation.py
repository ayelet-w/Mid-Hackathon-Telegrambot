

class PersonLocation:
    def __init__(self, person_id, lat, lon, is_mask, datetime_start, duration, is_open_space):
        self.person_id = person_id
        self.lat = lat
        self.lon = lon
        self.is_mask = is_mask
        self.datetime_start = datetime_start
        self.duration = duration
        self.is_open_space = is_open_space



